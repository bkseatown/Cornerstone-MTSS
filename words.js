// ==========================================
// THE MAGIC AUTO-TAGGER (Prevents Repetition)
// ==========================================
// This script runs automatically when the app loads.
// It adds extra tags to words so they appear in more categories.
// Example: "bridge" was tagged "trigraph". This adds "short-i" and "blend".

(function autoTagWords() {
    const entries = window.WORD_ENTRIES || {};
    
    for (const word in entries) {
        const data = entries[word];
        const tags = data.tags || [];

        // 1. Auto-tag Syllable Count
        if (data.syllables.includes("-")) {
            if (!tags.includes("multisyllable")) tags.push("multisyllable");
        }

        // 2. Auto-tag CVC (Consonant-Vowel-Consonant)
        // If it's 3 letters and NOT "the", "she", etc.
        if (word.length === 3 && !tags.includes("cvc")) {
            const vowels = ['a','e','i','o','u'];
            if (!vowels.includes(word[0]) && vowels.includes(word[1]) && !vowels.includes(word[2])) {
                 tags.push("cvc");
            }
        }

        // 3. Auto-tag High Frequency (The "Dolch" List Check)
        const common = ["the","and","was","for","are","with","his","they","this","have","from","one","had","word","but","not","what","all","were","when","your","can","said","there","use","an","each","which","she","do","how","their","if"];
        if (common.includes(word) && !tags.includes("high-freq")) {
            tags.push("high-freq");
        }

        // 4. Auto-tag Science/Academic based on Definition
        const scienceKeywords = ["organism", "planet", "energy", "liquid", "gas", "solid", "species", "mammal", "insect", "space", "earth", "moon"];
        if (scienceKeywords.some(keyword => data.def.toLowerCase().includes(keyword))) {
            if (!tags.includes("science")) tags.push("science");
        }

        // Save back
        entries[word].tags = tags;
    }
})();


